public class Arithmetic_operators {
    public static void main(String args []){
        //Binary Operators
        int A=65;
        int B=76;
        System.out.println("Add ="+(A+B));

        //Unary Operators
        int a=5;
        int b=++a;
        System.out.println(a);
        System.out.println(b);

        int c=7;
        int d=c++;
        System.out.println(c);
        System.out.println(d);

    }
}